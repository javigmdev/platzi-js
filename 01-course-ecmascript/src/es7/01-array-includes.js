//includes method: returns true if an array contains a specified value, return false if the value is not found
let numbers = [1, 3, 4, 5, 2, 1];
console.log(numbers.includes(4));
console.log(numbers.includes(7));

const names = ['Tom', 'David', 'Mary'];
console.log(names.includes('David'));
console.log(names.includes('Lily'));
